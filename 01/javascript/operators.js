// arithmetic

// Addition
var sum = 2 + 3; // sum = 5

// Subtraction
var diff = 5 - 2; // diff = 3

// Multiplication
var product = 2 * 3; // product = 6

// Division
var quotient = 10 / 2; // quotient = 5

// Modulus
var remainder = 10 % 3; // remainder = 1

// comparison
// Equality operator
var isEqual = 5 == 5; // isEqual = true

// Inequality operator
var isNotEqual = 5 != 5; // isNotEqual = false

// Strict equality operator
var isStrictEqual = 5 === '5'; // isStrictEqual = false

// Strict inequality operator
var isStrictNotEqual = 5 !== '5'; // isStrictNotEqual = true

// Greater than operator
var isGreaterThan = 5 > 3; // isGreaterThan = true

// Less than operator
var isLessThan = 5 < 3; // isLessThan = false

// Greater than or equal to operator
var isGreaterThanOrEqual = 5 >= 5; // isGreaterThanOrEqual = true

// logical operators

// Logical AND operator
var isTrue = true && true; // isTrue = true

// Logical OR operator
var isTrue = true || false; // isTrue = true

// Logical NOT operator
var isFalse = !true; // isFalse = false

// assignment operators
// Simple assignment
var x = 5;

// Addition assignment
var y = 2;
y += 3; // y = 5

// Subtraction assignment
var z = 5;
z -= 2; // z = 3

// Multiplication assignment
var w = 2;
w *= 3; // w = 6

// Division assignment
var p = 10;
p /= 2; // p = 5
